    public string this[string key]
    {
        get
        {
            var dictionary = IsArabic ? Arabic : English;
            return dictionary.TryGetValue(key, out var value)
                ? value
                : Arabic.TryGetValue(key, out var fallback) ? fallback : key;
        }
    }

    public bool IsArabic => CultureInfo.CurrentUICulture.TwoLetterISOLanguageName.Equals("ar", StringComparison.OrdinalIgnoreCase);
    public string CultureName => IsArabic ? "ar-EG" : "en-US";
    public string OtherCultureName => IsArabic ? "en-US" : "ar-EG";
    public string Direction => IsArabic ? "rtl" : "ltr";
    public string LanguageToggleLabel => IsArabic ? "English" : "العربية";
